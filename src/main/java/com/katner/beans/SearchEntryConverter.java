package com.katner.beans;

import javax.faces.convert.Converter;
import javax.faces.context.FacesContext;
import javax.faces.component.UIComponent;

import com.katner.model.SearchEntry;

public class SearchEntryConverter implements Converter {

    public SearchEntryConverter() {
    }

    public Object getAsObject(FacesContext facesContext, UIComponent uIComponent, String string) {
        if (string == null || string.trim().length() == 0) {
            return null;
        }

        SearchEntryBean managedBean = (SearchEntryBean) facesContext.getApplication().getVariableResolver().resolveVariable(
                facesContext, "searchEntry");

        final int id = Integer.parseInt(string);

        return managedBean.findEntity(id);
    }

    public String getAsString(FacesContext facesContext, UIComponent uIComponent, Object object) {
        if (object == null) return null;

        if (object instanceof SearchEntry) {
            SearchEntry entity = (SearchEntry) object;

            final String pk = String.valueOf(entity.getId());

            return pk;
        } else {
            throw new IllegalArgumentException("Incorrect object type: " + object.getClass().getName() + "; must be: SearchEntry");
        }
    }
}
